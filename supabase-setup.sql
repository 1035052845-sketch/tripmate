create table if not exists public.shared_rooms (
  id bigint generated by default as identity primary key,
  trip_code text not null default 'SEOUL630',
  room_number text not null,
  room_type text not null,
  room_detail text not null,
  members text[] not null default '{}',
  updated_at timestamptz not null default now(),
  unique (trip_code, room_number)
);

alter table public.shared_rooms enable row level security;
alter table public.shared_rooms replica identity full;

drop policy if exists "Anyone can view shared rooms" on public.shared_rooms;
create policy "Anyone can view shared rooms"
on public.shared_rooms for select to anon, authenticated
using (trip_code = 'SEOUL630');

drop policy if exists "Anyone can add shared rooms" on public.shared_rooms;
create policy "Anyone can add shared rooms"
on public.shared_rooms for insert to anon, authenticated
with check (trip_code = 'SEOUL630');

drop policy if exists "Anyone can update shared rooms" on public.shared_rooms;
create policy "Anyone can update shared rooms"
on public.shared_rooms for update to anon, authenticated
using (trip_code = 'SEOUL630')
with check (trip_code = 'SEOUL630');

