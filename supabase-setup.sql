-- 在 Supabase 项目左侧打开 SQL Editor，粘贴整份脚本，然后点击 Run。

create table if not exists public.shared_expenses (
  id bigint generated by default as identity primary key,
  trip_code text not null default 'SEOUL630',
  title text not null,
  amount numeric(12, 2) not null check (amount > 0),
  payer text not null,
  category text not null check (category in ('lodging', 'food', 'transport', 'fun')),
  participants text[] not null,
  created_at timestamptz not null default now()
);

alter table public.shared_expenses enable row level security;
alter table public.shared_expenses replica identity full;

drop policy if exists "Anyone can view shared trip expenses" on public.shared_expenses;
create policy "Anyone can view shared trip expenses"
on public.shared_expenses for select
to anon, authenticated
using (trip_code = 'SEOUL630');

drop policy if exists "Anyone can add shared trip expenses" on public.shared_expenses;
create policy "Anyone can add shared trip expenses"
on public.shared_expenses for insert
to anon, authenticated
with check (
  trip_code = 'SEOUL630'
  and payer in ('lulu', 'jiajia', 'xiaobei', 'ahuang', 'momo', 'anan')
  and cardinality(participants) > 0
);

drop policy if exists "Anyone can delete shared trip expenses" on public.shared_expenses;
create policy "Anyone can delete shared trip expenses"
on public.shared_expenses for delete
to anon, authenticated
using (trip_code = 'SEOUL630');

create table if not exists public.shared_plans (
